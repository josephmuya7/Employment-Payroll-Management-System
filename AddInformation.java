    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String na, idx, sub, add, sess, phone, emails, xy;
        na = name.getText();
        idx = id.getText();
        sub = subject.getText();
        add = addr.getText();
        sess = session.getText();
        phone = phne.getText();
        emails = email.getText();
       
        if (pp == null) {
            err.setText("No Image Selected");
            err.setForeground(Color.red);
            return;
        }
        if (idx.equals("") || na.equals("") || sub.equals("") || add.equals("") || sess.equals("") || phone.equals("") || emails.equals("")) {
            err.setText("All Field Need to be Filled");
            err.setForeground(Color.red);
            return;
        }

        if (!emails.matches(EMAIL_PATTERN)) {
            err.setText("Please Enter a Valid Email");
            err.setForeground(Color.red);
            return;
        }

        try {
            String url = "jdbc:sqlserver://DESKTOP-KM1V429\\SQLEXPRESS;databaseName=Student;integratedSecurity=true";
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection conn = DriverManager.getConnection(url);

            String SQL = "SELECT * FROM STUDENTINFO WHERE ID = " + "'" + idx + "'";
            PreparedStatement ps = null;
            ps = conn.prepareStatement(SQL);
            ResultSet rs = null;
            rs = ps.executeQuery();

            int coun = 0;
            while (rs.next()) {
                coun++;
            }
            if (coun >= 1) {
                err.setText("This ID already Registered!!!");
                err.setForeground(Color.red);
                return;
            } else {
                SQL = "insert into studentinfo values(?,?,?,?,?,?,?,?,?)";
                ps = conn.prepareStatement(SQL);
                ps.setString(1, idx);
                ps.setString(2, na);
                ps.setString(3, sub);
                ps.setString(4, sess);
                ps.setString(5, add);
                ps.setString(6, phone);
                ps.setString(7, emails);
                FileInputStream fin = new FileInputStream(pp);
                ps.setBinaryStream(8, (InputStream) fin, (int) pp.length());
                
                ps.executeUpdate();
                //System.out.println("Successfully inserted the file into the database!");
                ps.close();
                conn.close();
                String[] myString = {na,sub,sess,idx,phone,emails,add};
                
                
                new GenerateQrCode(idx);
                new GeneratePdf(myString,pp);
                
                err.setText("");
                

                //JOptionPane.showMessageDialog(null, "Information Added to the data base", "Done", JOptionPane.OK_OPTION);

            }

        } catch (ClassNotFoundException | SQLException | FileNotFoundException ex) {
            Logger.getLogger(AddInformation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException | IOException ex) {
            Logger.getLogger(AddInformation.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

